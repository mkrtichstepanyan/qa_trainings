package javaClasses.homework_4.stepan_alvajyan;

public class Calculations {

    double getMean(int a, int b) {
        return (a + b) / 2;
    }

    double getMean(int a, int b, int c) {
        return (a + b + c) / 3;
    }

    double getMean(double a, double b, double c) {
        return (a + b + c) / 3;
    }

    double getMean(float a, float b) {
        return (a + b) / 2;
    }

    String getMean(String a, String b, String c) {
        return a + b + c;
    }
}
